package org.maven;

public class for_loop {
    public static void main(String[] args) {
        int a = 10;//initializing variable
        //declaring the for loop
        // for loop has 3 statements in it. Initialization,Condition and Step(Increment / Decrement)
        //  Syntax : for(Initialization ; Condition ; Step(Increment/Decrement){
        // statements;
        // }
        for (int i = 0; i < a; i++) {
            System.out.println("\n i:" + i);//printing i values from 0 to 9
        }
    }
}
