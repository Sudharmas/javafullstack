package org.maven;

public class while_loop {
    public static void main(String[] args) {
        int i = 0;
        //declaring while loop
        // Syntax: while(condition) {
        // if the condition is true the execute the statements;
        // else leave the while loop
        // }
        while(i<=10){//if condtition is true then execute the statement
            System.out.println("i:"+i);//print value of i
            i++; //incrementing value of i by one

        }
    }
}
