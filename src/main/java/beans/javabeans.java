package beans;

public class javabeans {

	private String fname;
	private String l_name;

	public javabeans() {
		fname = fname;
		l_name = l_name;
	}
	public String getfname() {
		return fname;
	}

	public void setfname(String fname) {
		this.fname = fname;
	}

	public String getL_name() {
		return l_name;
	}

	public void setL_name(String l_name) {
		this.l_name = l_name;
	}
}
