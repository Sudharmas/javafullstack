package classes;

public class classandobjects { // creating a class named classandobjects
    // private is a access specifier,it's used to declare variables and methods so that they cannot be accessed or used outside the class that they have been declared 
    // they can only be used inside the class where they have been declared
    private String color;
    private int number;
    private String name;
    // public is a access specifier , variables declared as public can be used in any class using objects.
    public int speed=100;
}
